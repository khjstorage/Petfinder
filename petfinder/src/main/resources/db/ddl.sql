CREATE TABLE MEMBER(
M_ID VARCHAR2(20),
M_PASSWORD VARCHAR2(20) NULL,
M_NAME VARCHAR2(20) NULL,
M_PHONE VARCHAR2(18) NULL,
M_REGION1 VARCHAR2(80) NULL,
M_REGION2 VARCHAR2(80) NULL,
M_REGION3 VARCHAR2(80) NULL,
M_DOG VARCHAR2(20) NULL,
M_SIZE VARCHAR2(10) NULL,
M_COLOR VARCHAR2(20) NULL,
M_GENDER VARCHAR2(4) NULL,
PRIMARY KEY(M_ID)
);

CREATE TABLE TB_FILE
(
  IDX   NUMBER,
  M_ID VARCHAR2(20) NULL,
  ORIGINAL_FILE_NAME VARCHAR2(260 BYTE) NULL,
  STORED_FILE_NAME VARCHAR2(36 BYTE) NULL,
  FILE_SIZE NUMBER NULL,
  PRIMARY KEY(IDX)
);

-- 시퀀스 생성
CREATE SEQUENCE SEQ_TB_FILE_IDX
  START WITH 1
  INCREMENT BY 1
  NOMAXVALUE
  NOCACHE;

--시퀀스 삭제
DROP SEQUENCE SEQ_TB_FILE_IDX;